def build_sequences(corpus: str):
    """Tokenize corpus and build n-gram training sequences."""
    tokenizer = Tokenizer()
    tokenizer.fit_on_texts([corpus])
    total_words = len(tokenizer.word_index) + 1

    input_sequences = []
    for line in corpus.strip().split("\n"):
        token_list = tokenizer.texts_to_sequences([line])[0]
        for i in range(1, len(token_list)):
            input_sequences.append(token_list[: i + 1])

    max_len = max(len(s) for s in input_sequences)
    padded = pad_sequences(input_sequences, maxlen=max_len, padding="pre")

    X = padded[:, :-1]
    y = padded[:, -1]

    return tokenizer, total_words, max_len, X, y


