def build_model(arch: str, total_words: int, max_len: int, units: int = 128) -> Sequential:
    """
    Factory: returns compiled model for 'rnn', 'lstm', 'gru'.
    Dropout added for regularization — small corpus overfits hard.
    """
    layer_map = {"rnn": SimpleRNN, "lstm": LSTM, "gru": GRU}
    assert arch in layer_map, f"Unknown arch: {arch}"

    model = Sequential(
        [
            Embedding(total_words, 64, input_length=max_len - 1),
            layer_map[arch](units, return_sequences=False),
            Dropout(0.2),
            Dense(total_words, activation="softmax"),
        ],
        name=f"{arch.upper()}_TextGen",
    )
    model.compile(
        loss="sparse_categorical_crossentropy",
        optimizer=tf.keras.optimizers.Adam(learning_rate=0.005),
        metrics=["accuracy"],
    )
    return model

