def generate_text(
    model: Sequential,
    tokenizer: Tokenizer,
    seed_text: str,
    max_len: int,
    next_words: int = 7,
    temperature: float = 0.8,
) -> str:
    """
    Temperature sampling (NOT just argmax).
    temperature < 1 → conservative | > 1 → creative
    """
    idx_to_word = {v: k for k, v in tokenizer.word_index.items()}
    result = seed_text

    for _ in range(next_words):
        token_list = tokenizer.texts_to_sequences([result])[0]
        token_list = pad_sequences([token_list], maxlen=max_len - 1, padding="pre")
        probs = model.predict(token_list, verbose=0)[0]

        probs = np.log(probs + 1e-8) / temperature
        probs = np.exp(probs) / np.sum(np.exp(probs))
        predicted_idx = np.random.choice(len(probs), p=probs)

        result += " " + idx_to_word.get(predicted_idx, "")

    return result.strip()


