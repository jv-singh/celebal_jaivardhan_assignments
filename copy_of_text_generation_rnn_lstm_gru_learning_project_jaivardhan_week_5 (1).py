def compute_perplexity(model, X, y):
    """
    Perplexity = exp(avg NLL). Lower = better language model.
    argmax accuracy is a weak proxy — perplexity is the real metric.
    """
    probs = model.predict(X, verbose=0)
    nll = -np.mean(np.log(probs[np.arange(len(y)), y] + 1e-8))
    return np.exp(nll)


